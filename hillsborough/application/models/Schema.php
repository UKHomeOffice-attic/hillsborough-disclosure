				return $dates['title'];
			}
			if (isset($dates['breakdown']))
			{
				foreach($dates['breakdown'] as $subdates)
				{
					if ($start == $subdates['start'] && $end == $subdates['end'])
					{
						return $subdates['title'];
					}					
				}
			}
		}
		
		return $return;
	}
	
	static public function getDateRangeTitles()
	{		
		return self::$dateRangeTitles;
	}
